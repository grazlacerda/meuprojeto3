let idade = parseInt(prompt("Digite sua idade:"));

if (idade >= 18) {
    alert("Você é maior de idade. Já pode Trabalhar.");
    } else if (idade >=10) {
    alert("Você é pré adolecente. Vá Brincar.");
} else if (idade >=12) {
    alert("Você é adolecente. Vá jogar Roblox.");
} else if (idade >=30) {
    alert("Você é adulto. Vá trabalhar!");
} else if (idade >=40) {
    alert("Você é um adulto maduro. Continue contribuindo!");
} else if (idade >=60) {
    alert("Você é um idoso. Aproveite a vida!");
}